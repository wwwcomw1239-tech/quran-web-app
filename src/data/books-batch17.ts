import type { BookCollection, BookVolume } from './books';

function mkVolumes(baseId: string, identifier: string, pdfs: string[]): BookVolume[] {
  const ar = ['الأول','الثاني','الثالث','الرابع','الخامس','السادس','السابع','الثامن','التاسع','العاشر',
    'الحادي عشر','الثاني عشر','الثالث عشر','الرابع عشر','الخامس عشر','السادس عشر','السابع عشر','الثامن عشر','التاسع عشر','العشرون',
    'الحادي والعشرون','الثاني والعشرون','الثالث والعشرون','الرابع والعشرون','الخامس والعشرون','السادس والعشرون','السابع والعشرون','الثامن والعشرون','التاسع والعشرون','الثلاثون'];
  if (pdfs.length === 1) {
    return [{ id: baseId + '-01', title: 'النسخة الكاملة', pdfUrl: `https://archive.org/download/${identifier}/${pdfs[0]}` }];
  }
  return pdfs.map((name, i) => ({
    id: baseId + '-' + String(i+1).padStart(2,'0'),
    title: 'الجزء ' + (ar[i] || String(i+1)),
    volumeNumber: i+1,
    pdfUrl: `https://archive.org/download/${identifier}/${name}`,
  }));
}

